package day01.d1type;

public class D01_scale {
    public static void main(String[] args) {
//         0B 开头代表二进制
        int i1 = 0B0111;// 7
        System.out.println(i1);
//         0 开头代表八进制
        int i2 = 0141;// 97
        System.out.println(i2);
//        0x 代表16进制
        int i3 = 0x61;// 97
        System.out.println(i3);


    }
}
